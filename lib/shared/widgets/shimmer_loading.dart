/// Shimmer list for flight results
class FlightListShimmer extends StatelessWidget {
  final int itemCount;

  const FlightListShimmer({
    super.key,
    this.itemCount = 5,
  });

  @override
  Widget build(BuildContext context) {
    return ListView.builder(
      padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 8),
      itemCount: itemCount,
      itemBuilder: (context, index) => const FlightCardShimmer(),
    );
  }
}

